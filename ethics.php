							<h2> Labour and Human Rights </h2>
							<h3>Child Labour</h3>
							<p>
							Manufacturers and suppliers will not use child
labour. The term “child” refers to:
<ul class='ethics-manual'>
<li>a person younger than 15 (or 14 where local law allows); or </li>
<li>the local legal minimum age for employment; or</li>
<li>the age for completing compulsory education.</li></ul></p>
<p>Manufacturers and suppliers who employ young
persons that do not fall within the definition of
“child” will also comply with any laws and
regulations applicable to such persons.</p>
<h3>Involuntary Labour</h3>
<p>Manufacturers and suppliers will not use any
forced or involuntary labour. This includes
prison, bonded, indentured or other involuntary
labour.</p>
<h3>Coercion and Harassment</h3>
<p>Manufacturers and suppliers will: 
<ul class='ethics-manual'>
<li>treat all employees and contractors with dignity and respect;</li>
<li>not use corporal punishment; and</li>
<li>not threaten employees and contractors with violence or other forms of physical, sexual, psychological or verbal harassment or abuse.</li>
</ul></p>
<h3>Non-Discrimination</h3>
<p>Manufacturers and suppliers must not discriminate
on the basis of:
<ul class='ethics-manual'>
<li>race;</li>
<li>colour;</li>
<li>gender;</li>
<li>sexual orientation;</li>
<li>age;</li>
<li>physical or mental disability;</li>
<li>marital status;</li>
<li>family or carer responsibilities;</li>
<li>pregnancy;</li>
<li>religion;</li>
<li>political opinion;</li>
<li>national extraction; and</li>
<li>social origin.</li></ul></p>
<h3>Association</h3>
<p>Manufacturers and suppliers will respect the
rights of employees to associate, organise and
bargain collectively in a lawful and peaceful
manner, without penalty or interference.</p>
<h3>Health and Safety</h3>
<p>Manufacturers and suppliers will provide employees
with:
<ul class='ethics-manual'>
<li>a safe and healthy workplace in compliance with all applicable laws and regulations;</li>
<li>reasonable access to potable water and sanitary facilities;</li>
<li>fire safety; and</li>
<li>adequate lighting and ventilation.</li></ul></p>
<h3>Compensation</h3>
<p>We expect manufacturers and suppliers to recognise
that wages are essential to meeting employees’
basic needs.</p>
<p>
Manufacturers and suppliers will comply with all
applicable wage and hour laws and regulations,
including those relating to:
<ul class='ethics-manual'>
<li>minimum wages;</li>
<li>overtime;</li>
<li>maximum hours;</li>
<li>piece rates and other elements of compensation; and</li>
<li>legally mandated benefits.</li>
</ul></p>
<p>Manufacturers and suppliers will compensate
employees for overtime hours as legally required
or, if there is no legally prescribed premium
rate, at a rate at least equal to the regular
hourly compensation rate.</p>
<p>Where local industry standards are higher than
applicable legal requirements, we expect
manufacturers and supplier to meet the higher
standards.
</p>

<h2>Protection of the Environment</h2>
<p>Manufacturers and suppliers will comply with all
applicable environmental laws and regulations.
</p>
<p>We expect manufacturers and suppliers to recognise
sustainable sourcing and sustainable manufacturing
processes to be of high importance.</p>
<p>
Where possible, manufacturers and suppliers should
strive to constantly implement and improve
environmentally friendly business practices.</p>
<h2>Ethical Business Practices</h2>
<p>We expect manufacturers and suppliers to act
ethically in all business dealings.</p>
<h3>Other Laws</h3>
<p>Manufacturers and suppliers will comply with all
applicable laws and regulations, including those
relating to the manufacture, pricing, sale and
distribution of our products.</p>
<p>All references to “applicable laws and
regulations” in this Code of Conduct and Ethics
Manual include local and national codes, rules and
regulations as well as applicable treaties and
voluntary industry standards.</p>
<h2>Welfare of Animals</h2>
<p>We expect manufacturers and suppliers ensure the
considerate and humane treatment of animals.</p>
<p>Manufacturers and suppliers will comply with all
applicable animal welfare laws and regulations,
including animal welfare codes of practice.</p>
